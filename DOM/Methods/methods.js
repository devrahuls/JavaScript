//createElement (If we want to create any tag/element in HTML with the help of JS then we got a method called 'createElement')
let newElement = document.createElement("p")
let newElement1 = document.createElement("h3")

console.log(newElement)
console.log(newElement1)



//createTextNode (If we want to create any text in HTML with the help of JS then we got a method called 'createTextNode')
let newText = document.createTextNode("This is just a Text")

console.log(newText)



//Tag (If we want to create any comment in HTML with the help of JS then we got a method called 'createComment')
let newComment = document.createComment("This is just a comment")

console.log(newComment)
